// Get village/hobli name based on language
export const getVillageName = (village, language = 'en') => {
	if (typeof village === 'string') return village;
	return village[language] || village.en;
};

export const getHobliName = (hobli, language = 'en') => {
	if (typeof hobli === 'string') return hobli;
	return hobli[language] || hobli.en;
};

// Helper function to format location with hobli, taluk and district
export const formatLocation = (
	villageName,
	hobliName = '',
	language = 'en'
) => {
	if (!villageName) return '';

	const labels =
		language === 'kn'
			? { hobli: 'ಹೋಬಳಿ', taluk: 'ತಾಲೂಕು', dist: 'ಜಿಲ್ಲೆ' }
			: { hobli: 'Hobli', taluk: 'Taluk', dist: 'Dist' };

	const magadi = language === 'kn' ? 'ಮಾಗಡಿ' : 'Magadi';
	const ramanagar = language === 'kn' ? 'ರಾಮನಗರ' : 'Ramanagar';

	if (hobliName) {
		return `${villageName}, ${hobliName} (${labels.hobli}), ${magadi} (${labels.taluk}), ${ramanagar} (${labels.dist})`;
	}
	return `${villageName}, ${magadi} (${labels.taluk}), ${ramanagar} (${labels.dist})`;
};

// Helper function to extract village name from formatted location
export const extractVillageName = (formattedLocation) => {
	if (!formattedLocation) return '';
	return formattedLocation.split(',')[0].trim();
};

// Helper function to extract hobli name from formatted location
export const extractHobliName = (formattedLocation) => {
	if (!formattedLocation) return '';
	const parts = formattedLocation.split(',');
	if (parts.length >= 4) {
		const hobliPart = parts[1].trim();
		return hobliPart.split('(')[0].trim();
	}
	return '';
};

export const localizeLocation = (locationStr, language) => {
	if (!locationStr) return '';
	const villageName = extractVillageName(locationStr);
	const hobliName = extractHobliName(locationStr);

	if (!villageName) return locationStr;

	// Find Village
	let villageMatch = MAGADI_VILLAGES.find(
		(v) =>
			v.en.toLowerCase() === villageName.toLowerCase() || v.kn === villageName
	);
	// Fallback if exact match not found (maybe custom input)
	let displayVillage = villageMatch
		? getVillageName(villageMatch, language)
		: villageName;

	// Find Hobli
	let displayHobli = '';
	if (hobliName) {
		let hobliMatch = MAGADI_HOBLIS.find(
			(h) => h.en.toLowerCase() === hobliName.toLowerCase() || h.kn === hobliName
		);
		displayHobli = hobliMatch ? getHobliName(hobliMatch, language) : hobliName;
	}

	return formatLocation(displayVillage, displayHobli, language);
};
